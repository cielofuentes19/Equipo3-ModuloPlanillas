    private void txtBusquedaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()== KeyEvent.VK_ENTER){     
            try {
                String sql = "Select * from empleado where CodigoEmpleado = ? ";
                ps = conn.prepareStatement(sql);
                ps.setString(1, txtBusqueda.getText());
                rs = ps.executeQuery();
                if (rs.next()){
                    txtDni.setText(rs.getString(1));
                    txtNombres.setText(rs.getString(2));
                    txtApellMa.setText(rs.getString(3));
                    txtApellPa.setText(rs.getString(4));
                    txtPais.setText(rs.getString(5));
                    if (rs.getString(6).equals("M")){
                        genero="M";
                        g_Mas.setSelected(true);
                        g_Fem.setSelected(false);
                    } else if (rs.getString(6).equals("F")){
                        genero="F";
                        g_Mas.setSelected(false);
                        g_Fem.setSelected(true);
                    }
                    
             
                    if (rs.getString(7).equals("s")){
                     txtEstadoCivil.setSelectedIndex(1);
                    } else if (rs.getString(7).equals("c")){
                     txtEstadoCivil.setSelectedIndex(2);
                    } else if (rs.getString(7).equals("v")){
                      txtEstadoCivil.setSelectedIndex(3);
                    }            
                    
                    
                    txtCelular.setText(rs.getString(8));
                    txtCorreo.setText(rs.getString(9));
                    txtDirec.setText(rs.getString(10));
                    
                    if (rs.getString(12).equals("Sub-Gerente")){
                     txtPuesto.setSelectedIndex(1);
                    } else if (rs.getString(12).equals("Farmaceutico")){
                     txtPuesto.setSelectedIndex(2);
                    } else if (rs.getString(12).equals("Tecnico en Farmarcia")){
                      txtPuesto.setSelectedIndex(3);
                    }   
                    
                    if (rs.getString(13).equals("Mañana")){
                     txtTurno.setSelectedIndex(1);
                    } else if (rs.getString(13).equals("Tarde")){
                     txtTurno.setSelectedIndex(2);
                    } else if (rs.getString(13).equals("Noche")){
                      txtTurno.setSelectedIndex(3);
                    } 
               
                    txtSueldo.setText(rs.getString(14));  
                    
                    
                }
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex);
            }
        }
    }//GEN-LAST:event_txtBusquedaKeyPressed

